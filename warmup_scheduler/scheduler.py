    from torchvision.models import resnet18
    from torch.optim.lr_scheduler import LambdaLR

    def func(epoch):
        if epoch < 40:
            return 0.5
        elif epoch < 70:
            return 0.5 ** 2
        elif epoch < 90:
            return 0.5 ** 3
        else:
            return 0.5 ** 4

    model = resnet18()
    optimizer = torch.optim.SGD(model.parameters(), lr=0.05, momentum=0.9, weight_decay=1e-5)
    scheduler = LambdaLR(optimizer, lr_lambda=func, verbose=True)

    for step in range(100):
        optimizer.step()
        scheduler.step()

    from matplotlib import pyplot as plt
    cos_list = []
    for i in range(100):
        cos_list.append(cos_annealing(base_lr=0.001, curr_epoch=i, T_max=100))

    plt.figure()
    plt.plot(range(100), cos_list)
    plt.show()
